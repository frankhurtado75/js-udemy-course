//Orden de operaciones

//Ejecuta primero * o /, luego + o -
resultado1 = 20 + 30 * 2;
console.log(resultado1);

//Ejecuta primero dentro de parentesis
resultado2 = ( 20 + 30 ) * 2;
console.log(resultado2);

//20% de descuento en tu carrito de compras
resultado3 = (20 + 30 + 40 + 50 + 60 ) * .2;
console.log(resultado3);

//Agregar 16%
resultado4 = (20 + 30) * 1.16;
console.log(resultado4);