//Aumentar y disminuir valores

//Incrementa el valor +1
let puntaje1 = 5;
puntaje1++; 
console.log(puntaje1);

let puntaje2 = 5;
++puntaje2;
console.log(puntaje2);

//Incrementa el valor +3
let puntaje3 = 5;
puntaje3+= 3;
console.log(puntaje3);

//Disminuye el valor -1
let puntaje4 = 5;
puntaje4--; 
console.log(puntaje4);

let puntaje5 = 5;
--puntaje5;
console.log(puntaje5);

//Disminuye el valor -3
let puntaje6 = 5;
puntaje6-= 3;
console.log(puntaje6);